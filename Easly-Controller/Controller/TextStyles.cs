namespace EaslyController.Constants
{
    /// <summary>
    /// Text styles supported for a IxxxTextValueFrame object.
    /// </summary>
    public enum TextStyles
    {
        /// <summary>
        /// The default style.
        /// </summary>
        Default,

        /// <summary>
        /// A single character.
        /// </summary>
        Character,

        /// <summary>
        /// A number.
        /// </summary>
        Number,

        /// <summary>
        /// A keyword.
        /// </summary>
        Keyword,

        /// <summary>
        /// A discrete value (enum or boolean).
        /// </summary>
        Discrete,

        /// <summary>
        /// A type identifier.
        /// </summary>
        Type,
    }
}
