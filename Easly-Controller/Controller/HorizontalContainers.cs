namespace EaslyController.Constants
{
    /// <summary>
    /// Container symbol around cells for a list or block list that contains more than one element.
    /// </summary>
    public enum HorizontalContainers
    {
        /// <summary>
        /// No symbol.
        /// </summary>
        None,

        /// <summary>
        /// Left and right brackets.
        /// </summary>
        Bracket,

        /// <summary>
        /// Left and right curly brackets.
        /// </summary>
        CurlyBracket,

        /// <summary>
        /// Left and right curly parenthesis.
        /// </summary>
        Parenthesis,
    }
}
