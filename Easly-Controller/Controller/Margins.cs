namespace NodeController
{
    /// <summary>
    /// Margins to use when drawing frames.
    /// </summary>
    public enum Margins
    {
        /// <summary>
        /// No margin.
        /// </summary>
        None,

        /// <summary>
        /// A thin margin.
        /// </summary>
        ThinSpace,

        /// <summary>
        /// A margin the width of a whitespace character.
        /// </summary>
        Whitespace,
    }
}
